public class L_10_Expre_Incre_Decre {
    public static void main(String[] args) {
        System.out.println("Increment and Decrement Operators");
//        Increment and Decrement Operators
        int a = 1;
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(--a);
        System.out.println(a--);
    }
}
